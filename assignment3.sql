CREATE TABLE EMP47 (
  EMPNO NUMBER(4) PRIMARY KEY CHECK (EMPNO BETWEEN 7000 AND 8000),
  ENAME VARCHAR2(10),
  JOB VARCHAR2(20),
  MGR NUMBER(4) CHECK (MGR BETWEEN 7000 AND 8000),
  HIREDATE DATE,
  SAL NUMBER(7,2),
  COMM NUMBER(6,2) DEFAULT 0,
  DEPT_NAME VARCHAR2(10),
  CHECK (
    (JOB = 'SALESMAN' AND COMM < 1500)
    OR
    (JOB <> 'SALESMAN' AND COMM IS NULL)
  )
);

