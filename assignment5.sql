CREATE TABLE ZIPCODES47
(
ZIP VARCHAR(6) PRIMARY KEY,
CITY VARCHAR(20) NOT NULL
);

CREATE TABLE EMPLOYEES47
(
ENO NUMBER(3) PRIMARY KEY,
ENAME VARCHAR(20) NOT NULL,
ZIP VARCHAR(6),
HDATE DATE,
FOREIGN KEY (ZIP) REFERENCES ZIPCODES47 (ZIP)
);

CREATE TABLE PARTS47(
PNO NUMBER(3) PRIMARY KEY,
PNAME VARCHAR(20) NOT NULL,
QOH NUMBER(10),
PRICE NUMBER(10),
LVL NUMBER(6)
);

CREATE TABLE CUSTOMERS47
(
CNO NUMBER(3) PRIMARY KEY,
CNAME VARCHAR(20) NOT NULL,
STREET VARCHAR(30),
ZIP VARCHAR(6),
PHONE NUMBER(10),
FOREIGN KEY (ZIP) REFERENCES ZIPCODES47 (ZIP)
);

CREATE TABLE ORDERS47
(
ONO NUMBER(3) PRIMARY KEY,
CNO NUMBER(3),
ENO NUMBER(3),
RECEIVED VARCHAR(3) CHECK (RECEIVED IN ('YES','NO')),
SHIPPED VARCHAR(3) CHECK (SHIPPED IN ('YES','NO')),
FOREIGN KEY (CNO) REFERENCES CUSTOMERS47 (CNO),
FOREIGN KEY (ENO) REFERENCES EMPLOYEES47 (ENO)
);

CREATE TABLE ODETAILS47
(
ONO NUMBER(3),
PNO NUMBER(3),
QTY NUMBER(6),
PRIMARY KEY (ONO,PNO),
FOREIGN KEY (ONO) REFERENCES ORDERS47 (ONO),
FOREIGN KEY (PNO) REFERENCES PARTS47 (PNO)
);

